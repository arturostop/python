print("Using range() and list convertion, generate the following list dynamically")

print("All numbers from 0 to 10 [0,1,2,...,10]")
print(list(range(11)))

print("All numbers from -10 to 0 [-10,-9,-8,...,0]")
print(list(range(-10,1)))

print("All even numbers from 0 to 20 [0,2,4,...20]")
print(list(range(0,21,2)))

print("All odd numbers between -20 and 0 [-19,-17-,15,...,-1]")
print(list(range(-19,0,2)))

print("All numbers multiples of 5 from 0 to 50 [0,5,10,...,50]")
print(list(range(0,51,5)))